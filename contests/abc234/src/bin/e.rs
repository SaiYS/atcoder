// #[fastout]
fn main() {
    input! {
        n: i64,
    }

    let mut a = (1..=9).collect_vec();

    for d in 2..=18 {
        for i in 1..=9 {
            'a: for j in 0..=9 {
                let mut p = i;
                for _ in 1..d {
                    let q = p % 10 + j - i;
                    if q > 9 || q < 0 {
                        continue 'a;
                    }
                    p = p * 10 + q;
                }
                a.push(p);
            }
        }
    }

    vis!(a.into_iter().find(|&x| x >= n).unwrap());
}

