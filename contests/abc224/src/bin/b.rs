// #[fastout]
fn main() {
    input! {
        h: usize, w: usize,
        a: [[usize; w]; h]
    }

    for i1 in 0..h {
        for i2 in 0..h {
            for j1 in 0..w {
                for j2 in 0..w {
                    if i1 < i2 && j1 < j2 {
                        if a[i1][j1] + a[i2][j2] > a[i1][j2] + a[i2][j1] {
                            vis!("No");
                            return;
                        }
                    }
                }
            }
        }
    }

    vis!("Yes");
}

