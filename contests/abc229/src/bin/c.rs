// #[fastout]
fn main() {
    input! {
        n: usize, w: usize,
        ch: [(usize, usize); n]
    }

    let mut ans = 0usize;
    let mut used = 0usize;
    for (a, b) in ch.into_iter().sorted_by_key(|x| Reverse(x.0)) {
        if used + b <= w {
            ans += a * b;
            used += b;
        } else {
            let t = w - used;
            ans += a * t;
            used += t;
        }
    }

    vis!(ans);
}

