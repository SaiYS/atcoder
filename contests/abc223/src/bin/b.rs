fn main() {
    input! {
        mut s: Chars
    }

    let mut t = Vec::new();
    for i in 0..s.len() {
        t.push(s.clone());
        s.rotate_left(1);
    }

    t.sort();
    let a = t[0].clone().into_iter().collect::<String>();
    let b = t[s.len() - 1].clone().into_iter().collect::<String>();
    vis!(a; b);
}

