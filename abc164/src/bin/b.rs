// #[fastout]
fn main() {
    input! {
        mut a: i64,
        b: i64,
        mut c: i64,
        d: i64,
    }

    for i in 0usize.. {
        if i.is_even() {
            c -= b;
            if c <= 0 {
                vis!("Yes");
                return;
            }
        } else {
            a -= d;
            if a <= 0 {
                vis!("No");
                return;
            }
        }
    }
}

