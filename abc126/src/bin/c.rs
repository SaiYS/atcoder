// #[fastout]
fn main() {
    input! {
        n: usize, k: usize,
    }

    let mut a = vec![0.0; k + 1];
    for i in 1..=min(n, k) {
        a[i] = 1.0 / n as f64;
    }

    for i in 1..k {
        if i * 2 <= k {
            a[i * 2] += a[i] / 2.0;
        }
        a[0] += a[i] / 2.0;
    }

    let ans = 1.0 - a[0];
    vis!(ans);
}

